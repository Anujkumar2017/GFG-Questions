Node* Reverse(Node* head){
    Node* current = head;
    Node *prev = NULL, *next = NULL;

    while (current != NULL) {
        // Store next
        next = current->next;
        // Reverse current node's pointer
        current->next = prev;
        // Move pointers one position ahead.
        prev = current;
        current = next;
    }
    return prev;
}

class Solution
{
    public:
    Node *compute(Node *head)
    {
        // reverse the list
        
        head=Reverse(head);
        Node *temp, *ans=head, *ansptr=head;
        temp=head->next;
        while(temp!=NULL){
            if(temp->data>=ansptr->data){
                ansptr->next=temp;
                ansptr=ansptr->next;
            }
            temp=temp->next;
        }
        ansptr->next=NULL;
        ans=Reverse(ans);
        return ans;
    }
    
};
   


